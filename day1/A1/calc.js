// function to add two numbers
exports.addNums = function(num1, num2) {
  return num1 + num2;
}

// function to subtract two numbers
exports.subNums = function(num1, num2) {
  return num1 - num2;
}

// function to multiply two numbers
exports.multNums = function(num1, num2) {
  return num1 * num2;
}

// function to divide two numbers
exports.divNums = function(num1, num2) {
  return num1 / num2;
}
